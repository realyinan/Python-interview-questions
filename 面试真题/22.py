# Python中的运算符优先级决定了在表达式中各个部分的运算顺序。以下是Python中运算符按优先级从高到低的排列顺序：

# 1. **括号**：`()` - 用来改变运算顺序
# 2. **幂运算**：`**`
# 3. **正负号**：`+x, -x, ~x`
# 4. **乘法、除法、取余和整除**：`*, /, %, //`
# 5. **加法和减法**：`+, -`
# 6. **移位运算**：`<<, >>`
# 7. **按位与**：`&`
# 8. **按位异或**：`^`
# 9. **按位或**：`|`
# 10. **比较运算**：`==, !=, >, >=, <, <=, is, is not, in, not in`
# 11. **布尔运算**：`not, and, or`

# 这些运算符在表达式中的优先级顺序决定了运算的先后顺序。例如，`2 + 3 * 4` 的结果是 `14`，因为乘法优先于加法。使用括号可以改变默认的优先级，如 `(2 + 3) * 4` 的结果是 `20`。


# 说一下你知道的Python编码规范。

# [python]: PEP-8 是 Python 编码的风格指南，旨在提高代码的可读性和一致性。下面是 PEP-8 的一些关键要点：

# 1. **缩进**：使用 4 个空格进行缩进。不使用 Tab 键。

# 2. **最大行长度**：每行代码的长度不超过 79 个字符，文档字符串或注释不超过 72 个字符。

# 3. **空行**：
#    - 顶级函数和类定义之前用两个空行。
#    - 类内的方法定义之间用一个空行。
#    - 函数或类内部的逻辑段落之间可以用空行分隔。

# 4. **源文件编码**：编码默认使用 UTF-8。

# 5. **导入**：
#    - 导入应该放在文件的顶部，在模块说明和文档字符串之后，模块全局变量之前。
#    - 按顺序导入标准库模块、第三方模块和本地应用/库的特定导入，每一组之间用空行分隔。
#    - 尽量使用绝对导入。

# 6. **空格的使用**：
#    - 使用空格而不是 Tab。
#    - 在逗号、分号、冒号之后应使用空格，但紧跟左括号、左方括号或左花括号后不使用空格。
#    - 函数的参数列表、索引或切片的左括号之前不加空格。
#    - 函数的默认参数值等号两边不加空格。

# 7. **注释**：
#    - 块注释应用 `#` 和一个空格，然后再写注释内容。
#    - 行尾注释尽量少用，应与代码至少间隔两个空格。
#    - 文档字符串使用三重引号 `"""`。

# 8. **命名约定**：
#    - 模块名尽量短小，使用全小写字母，可以使用下划线。
#    - 类名使用驼峰命名法（CapWords）。
#    - 函数名、变量名和属性名用小写字母，可以使用下划线。
#    - 常量全部大写字母，用下划线分隔。

# 9. **代码布局**：
#    - 操作符两边各有一个空格，但对于紧凑表达式（例如函数参数中的 `=`）除外。
#    - 不要在函数的定义与调用中紧贴括号内部放置空格。

# 这是 PEP-8 的一些核心要点，遵守这些规则可以使代码更加整洁，易于阅读和维护。如果有任何具体的 PEP-8 相关问题或需要更多示例，请告诉我。
